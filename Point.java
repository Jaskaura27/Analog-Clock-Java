public class Point {
    //Declare the private variable for class Point.
    Double x,y;

    //Constructor
    public Point(Double x, Double y){
        this.x=x;
        this.y=y;
    }

    //returns x coordinate.
    public Double getX(){
        return x;
    }

    //returns y coordinate.
    public Double getY(){
        return y;
    }

}